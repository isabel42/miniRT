#include "libft.h"

t_vec3d	ft_v_add(t_vec3d v1, t_vec3d v2)
{
	t_vec3d	res;

	res.x = v1.x + v2.x;
	res.y = v1.y + v2.y;
	res.z = v1.z + v2.z;
	return (res);
}
